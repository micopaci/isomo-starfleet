-- Migration 044: QR Asset Tags + Intake Kiosk Operators
-- Ties the 500 pre-printed QR stickers (asset_number) to devices, and adds
-- per-intern PIN operators for the scan-to-register kiosk flow.
-- Reuses device_assignments + device_lifecycle_logs from migration 034.

-- 1. Asset tags: one row per pre-printed QR sticker (asset_number <-> qr_token).
--    device_id is bound at registration time and is permanent 1:1 per physical laptop.
CREATE TABLE IF NOT EXISTS asset_tags (
  asset_number INTEGER PRIMARY KEY,
  qr_token     TEXT UNIQUE NOT NULL,
  device_id    INTEGER REFERENCES devices(id) ON DELETE SET NULL,
  bound_at     TIMESTAMPTZ,
  bound_by     TEXT,
  created_at   TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_asset_tags_token ON asset_tags(qr_token);
-- Enforce permanent 1:1: a device may be bound to at most one asset tag.
CREATE UNIQUE INDEX IF NOT EXISTS idx_asset_tags_device_unique
  ON asset_tags(device_id) WHERE device_id IS NOT NULL;

-- 2. Intake kiosk operators (per-intern PIN). pin_hash is a bcrypt hash; never
--    store plaintext PINs. The matched operator identity is written to
--    device_lifecycle_logs.operator_email for every scan action.
CREATE TABLE IF NOT EXISTS intake_operators (
  id         SERIAL PRIMARY KEY,
  name       TEXT NOT NULL,
  email      TEXT UNIQUE,
  pin_hash   TEXT NOT NULL,
  active     BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  last_used_at TIMESTAMPTZ
);

